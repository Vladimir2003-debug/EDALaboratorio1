public class Math {
    public static void main(String[] args) {
        int x = 4, y = 3;
        //maximo de dos numeros
        Math.max(x, y);
        //minimo de dos numeros
        Math.min(x, y);
        //raiz cuadrada
        Math.sqrt(64);
        //valor absoluto
        Math.abs(4.56); //result 4;
        //valor random
        Math.random(); //resultado entre 0(inclusivo) y 1(exlusivo);

    }
}