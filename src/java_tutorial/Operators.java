
public class Operators {
    public static void main(String[] args) {
        int x = 5, y = 3;
        //suma
        System.out.println(x + y);
        //sustraccion
        System.out.println(x - y);
        //multuitiplicacion
        System.out.println(x * y);
        // division
        System.out.println(x / y);
        // Modulo
        System.out.println(x % y);
        // incremento x = 5 
        x++;
        System.out.println(x);
        // decremento x = 6
        x--;
        System.out.println(x);

       
    }
}