public class Booleans{
    public static void main(String[] args) {
        boolean txt1 = true;
        boolean txt2 = false;
        System.out.println(txt1);//resultado: true
        System.out.println(txt2);//resultado: false

        int x = 5, y = 3;
        //greater than
        System.out.println(x > y);//resultado: true
        //less than
        System.out.println(x < y);//resultado: false
        //equal to
        System.out.println(x == y);//resultado: false
    }
}